def read_dicom(file_path):
    RESIZE_TO_PIXELSPACING = 0.2
    obj = pydicom.dcmread(file_path)
    if obj.PixelSpacing[0] != obj.PixelSpacing[1]:
        raise UserWarning("Diffrent spacing {} ".format(obj.PixelSpacing))
    img = obj.pixel_array

    img, spacing, img_type = img, obj.PixelSpacing[0], obj.PhotometricInterpretation
    ratio = spacing / RESIZE_TO_PIXELSPACING
    shape = [int(s*ratio) for s in img.shape]

    img = cv2.resize(img, dsize=(shape[1], shape[0]), interpolation=cv2.INTER_CUBIC)

    img = img.astype(np.float) * 255. / img.max()
    img = img.astype(np.uint8)
    if img_type == "MONOCHROME1":
        img = np.invert(img)
    return img   
    
